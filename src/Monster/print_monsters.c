void my_display_monsters(Monster *enemies, int count) {

    int diff = 25;

    for (int line = 0; line < ASCII_LINES_M; line++) {
        printf("%*s", diff, "");
        for (int i = 0; i < count; i++) {
            printf("%s", monster_art[line]);
            if (i < count - 1) {
                printf("    ");
            }
        }
        printf("\n");
    }
    for (int i = 0; i < count; i++) {
        printf("%*sH: %d, A: %d-%d, D: %d ", diff, "", enemies[i].health, enemies[i].min_attack, enemies[i].max_attack, enemies[i].defense);
        if (i < count - 1) {
            printf("    ");
        }
    }
    printf("\n\n");
}


